/*  Using the Comma
  There will be times when we will want to include more than one statement in the initialization and iteration portions
  of the for loop.
*/
  class sample {
    public static void main(String args[]) {
      int a, b;

      for(a=1; a<b; a++) {
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        b--;
      }
    }
}
